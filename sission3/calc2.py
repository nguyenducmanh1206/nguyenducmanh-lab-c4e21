def calculate(x, y , op):
    result = 0
    if op == "+":
       result = x + y # print(x + y)
    elif op == "-":
       result = x - y # printt(x - y)
    elif op == "*":
       result = x * y # print(x * y)
    elif op == "/":
        result = x / y # print(x / y)
    return result
